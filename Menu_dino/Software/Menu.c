u8 KEY_Scan(u8 mode)
{
	static u8 key_up=1;
	if(key_up&&(KEY0==0||KEY1==0||WK_UP==0))
	{
		Delay_ms(500);
		key_up=0;
		if(KEY0==0) return 3;
		else if(KEY1==0) return 2;
		else if (WK_UP==0) return 1;
	}
	else  key_up=1;
	return 0;
}

